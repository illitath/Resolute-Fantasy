def get_display_width(text):
    width = 0
    for char in text:
        if '\u4e00' <= char <= '\u9fff':
            width += 2
        else:
            width += 1
    width = floor(width)
    return width


def pad_chinese_text(text, target_width, fillchar=' '):
    current_width = get_display_width(text)
    if current_width >= target_width:
        return text
    return text + fillchar * (target_width - current_width)
